#include <stdio.h>
#include <ctype.h>
#include "libft.h"

int	ft_tolower(int a)
{
	char	res;

	if ((a >= 65) && (a <= 90))
	{
		res = a - 'A' + 'a';
		return (res);
	}
	return (a);
}
